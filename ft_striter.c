#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t	i;

	if (!s)
		return ;
	if (!f)
		return ;
	i = 0;
	while (i != ft_strlen(s))
	{
		f(&s[i]);
		i++;
	}
}
